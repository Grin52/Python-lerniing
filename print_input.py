#           У этой функции имеется два необязательных именованных параметра 
#           sep – разделитель между данными;
#           end – завершающий символ или строка.

a = -6.84
b = 7
c = 25.6
print(a, b, c)             # значения выводятся через пробел
print(a, b, c, sep=" | ")  # результат     6.84 | 7 | 25.6

print("Hello")            
print("World")             # выведет на экран следующее слово с новой строки

print("Hello", end=" ")
print("World")             # 